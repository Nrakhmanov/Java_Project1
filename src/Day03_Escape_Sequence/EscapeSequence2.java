package Day03_Escape_Sequence;

public class EscapeSequence2 {
    public static void main(String[] args){
        System.out.println("\"Iphone case\", \n \"XBox\",\n \"webcam\", \n \"monitor\"");
        System.out.println("\"Iphone case\", \t \"XBox\",\t \"webcam\", \t \"monitor\"");
        System.out.println();
        System.out.println("    ^   ");
        System.out.println("  /   \\  ");
        System.out.println(" |     | ");
        System.out.println("  -----  ");





    }
}
