package Day03_Escape_Sequence;

public class EscapeSequence {
    public static void main(String [] args){
        System.out.println("I like \"Java\" program");
        System.out.println("Lets buy \"coffee\" and \"tea\" \\");
        System.out.println("java\nintellij");
        System.out.println("java\tintellij");
        System.out.println("java\n\nintellij");
        System.out.println("java\n\tintellij");

    }
}
