package day21_string_manipulations;

public class StringIndexOf {
    public static void main(String[] args) {
             //        01234567
        String word = "Bambaloo";
        System.out.println(word.indexOf("B")); //0
        System.out.println(word.indexOf("loo")); //5
        System.out.println(word.indexOf("x")); // -1 because cannot find that character


    }
}
